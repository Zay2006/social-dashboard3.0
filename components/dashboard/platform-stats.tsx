"use client";

import { useEffect, useState } from "react";
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card";
import { formatNumber, formatPercentage, getGrowthColor } from "@/lib/utils";
import { ArrowDown, ArrowUp, Minus } from "lucide-react";

interface PlatformStats {
  id: number;
  name: string;
  icon: string;
  color: string;
  followers: number;
  growth: number;
}

export function PlatformStats() {
  const [platforms, setPlatforms] = useState<PlatformStats[]>([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    async function fetchPlatformStats() {
      try {
        setLoading(true);
        const response = await fetch("/api/stats/platform");
        
        if (!response.ok) {
          throw new Error("Failed to fetch platform statistics");
        }
        
        const data = await response.json();
        setPlatforms(data);
      } catch (err) {
        console.error("Error fetching platform stats:", err);
        setError("Failed to load platform statistics");
      } finally {
        setLoading(false);
      }
    }

    fetchPlatformStats();
  }, []);

  if (loading) {
    return (
      <div className="grid gap-4 md:grid-cols-2 lg:grid-cols-3">
        {[...Array(6)].map((_, i) => (
          <Card key={i} className="overflow-hidden">
            <CardHeader className="pb-2">
              <CardTitle className="text-sm font-medium">Loading...</CardTitle>
            </CardHeader>
            <CardContent>
              <div className="text-2xl font-bold animate-pulse bg-muted h-8 w-24 rounded"></div>
              <p className="text-xs text-muted-foreground animate-pulse bg-muted h-4 w-32 mt-2 rounded"></p>
            </CardContent>
          </Card>
        ))}
      </div>
    );
  }

  if (error) {
    return (
      <div className="rounded-md bg-destructive/15 p-4">
        <div className="flex">
          <div className="flex-shrink-0">
            <svg className="h-5 w-5 text-destructive" viewBox="0 0 20 20" fill="currentColor">
              <path fillRule="evenodd" d="M10 18a8 8 0 100-16 8 8 0 000 16zM8.707 7.293a1 1 0 00-1.414 1.414L8.586 10l-1.293 1.293a1 1 0 101.414 1.414L10 11.414l1.293 1.293a1 1 0 001.414-1.414L11.414 10l1.293-1.293a1 1 0 00-1.414-1.414L10 8.586 8.707 7.293z" clipRule="evenodd" />
            </svg>
          </div>
          <div className="ml-3">
            <h3 className="text-sm font-medium text-destructive">Error</h3>
            <div className="mt-2 text-sm text-destructive/80">
              <p>{error}</p>
            </div>
          </div>
        </div>
      </div>
    );
  }

  return (
    <div className="grid gap-4 md:grid-cols-2 lg:grid-cols-3">
      {platforms.map((platform) => (
        <Card key={platform.id} className="overflow-hidden">
          <div className="h-1" style={{ backgroundColor: platform.color }}></div>
          <CardHeader className="pb-2">
            <CardTitle className="text-sm font-medium flex items-center gap-2">
              {getPlatformIcon(platform.icon)}
              {platform.name}
            </CardTitle>
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold">{formatNumber(platform.followers)}</div>
            <p className="text-xs flex items-center gap-1">
              <span className={getGrowthColor(platform.growth)}>
                {platform.growth > 0 ? (
                  <ArrowUp className="h-3 w-3" />
                ) : platform.growth < 0 ? (
                  <ArrowDown className="h-3 w-3" />
                ) : (
                  <Minus className="h-3 w-3" />
                )}
                {formatPercentage(platform.growth)}
              </span>
              <span className="text-muted-foreground">since last month</span>
            </p>
          </CardContent>
        </Card>
      ))}
    </div>
  );
}

